//Exercise 39 City Countries
function city_country(city: string, country: string): string {
    return `${city}, ${country}`;
}

const city1 = city_country("Karachi", "Pakistan");
const city2 = city_country("Delhi", "India");
const city3 = city_country("Abu Dhabi", "UAE");

console.log(city1);
console.log(city2);
console.log(city3);
