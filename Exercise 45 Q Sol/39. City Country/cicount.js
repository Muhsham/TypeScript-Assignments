//Exercise 39 City Countries
function city_country(city, country) {
    return "".concat(city, ", ").concat(country);
}
var city1 = city_country("Karachi", "Pakistan");
var city2 = city_country("Delhi", "India");
var city3 = city_country("Abu Dhabi", "UAE");
console.log(city1);
console.log(city2);
console.log(city3);
